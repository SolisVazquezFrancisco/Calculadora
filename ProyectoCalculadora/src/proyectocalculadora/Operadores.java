
package proyectocalculadora;

public class Operadores {

    /**
    *Este metodo devuelve la suma de dos numeros en formato int
    * @param a numero requerido para deveolver int
    * @param b numero requerido para deveolver int
    * @return devuelde el resultado de la suma
    **/
    
    public int suma(int a, int b){
        return a+b;
    }
    
    /**
    *Este metodo devuelve la resta de dos numeros en formato int
    * @param a numero requerido para devolver int
    * @param b numero requerido para devolver int
    * @return devulve el resultado de la resta
    **/
    
    public int resta(int a, int b){
        return a-b;
    }
    
    /**
    *Este metodo devuelve la multiplicacion de dos numeros en formato int
    * @param a numero requerido para devolver int
    * @param b numero requerido para devolver int
    * @return devuelve el resultado de la multiplicacion
    **/
    
    public int mult(int a, int b){
        return a*b;
    }
    
    /**
    *Este metodo devuelve la division de dos numeros en formato int
    * @param a numero requerido para devolver int
    * @param b numero requerido para devolver int
    * @return devuelve el resultado de la division
    **/
    
    public int div(int a, int b){
        return a/b;
    }
}
